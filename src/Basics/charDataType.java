package Basics;

public class charDataType {
    public static void main(String[] args) {
        char ch = 'e';
        System.out.println(ch);
        char ch1 = '+';
        System.out.println(ch1);
        // ascii of 'a' is 97
        // ascii of 'A' is 65
        // ascii of 0 is 48

        System.out.println((int) ch);
    }
}
